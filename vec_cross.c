#include "vec_utils.h"

t_vec3	vec3_cross(t_vec3 a, t_vec3 b)
{
	t_vec3 new;

	new.x = a.y * b.z - a.z * b.y;
	new.y = a.z * b.x - a.x * b.z;
	new.z = a.x * b.y - a.y * b.x;
	return (new);
}
